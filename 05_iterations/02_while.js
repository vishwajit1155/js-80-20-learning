// while
let i = 0;
while (i <= 10) {
  // console.log(`value of i ${i}`);
  i++;
}

let myArray = ["BATMAN", "SUPERMAN", "FLASH"];
let arr = 0;
while (arr < myArray.length) {
  // console.log(`value of arr ${myArray[arr]}`);
  arr++;
}

// do while
let score = 11;
do {
  console.log(`score is ${score}`);
  score++;
} while (score <= 8); //run at least one time
